/**
 * _strcpy - A func that copies a string
 * Return: A pointer to *dest.
 * @dest: Final destination for the string
 * @src: Initial destination for the string
 */

char *_strcpy(char *dest, char *src)
{
	char *c = dest;

	while
	((*dest++ = *src++));

	return (c);
}
